function renderScriptNameFromToScriptIso(): string {
  return [
    header(),
    'public import UnicodeBasicCommon.Types.Script',
    'public import UnicodeBasicCommon.Types.MaybeUnknownScript',
    'public import UnicodeBasic.Types.ScriptName.Basic',
    'public import UnicodeBasic.Types.ScriptName.ToScript',
    'public import UnicodeBasic.Types.ScriptName.FromScript',
    '',
    exposeSection(),
    'namespace Unicode',
    '',
    'theorem if_val_eq {α : Type} (cond : Prop) [Decidable cond] (val1 val2 : α) (c : α) :',
    '  ((if cond then val1 else val2) = c) ↔ (if cond then c = val1 else c = val2) := by',
    '  split <;> simp [eq_comm]',
    '',
    'theorem if_val_eq2 {α : Type} (cond : Prop) [Decidable cond] (val1 val2 : α) (c : α) :',
    '  (c = (if cond then val1 else val2)) ↔ (if cond then c = val1 else c = val2) := by',
    '  split <;> simp',
    '',
    'theorem Script_code_if (cond : Prop) [Decidable cond] (a b : Script) :',
    '  (if cond then a else b).code = if cond then a.code else b.code := by',
    '  split <;> rfl',
    '',
    'theorem Script.ext {s1 s2 : Script} (h : s1.code = s2.code) : s1 = s2 := by',
    '  cases s1; cases s2; dsimp at h; subst h; rfl',
    '',
    'namespace ScriptName',
    '',
    'theorem toScript_if (cond : Prop) [Decidable cond] (a b : ScriptName) :',
    '  toScript (if cond then a else b) = if cond then toScript a else toScript b := by',
    '  split <;> rfl',
    '',
    'theorem fromToScriptIso (name : ScriptName) : fromScript (toScript name) = name := by',
    '  cases name <;> rfl',
    '',
    'theorem toFromScriptIso (s : Script) : toScript (fromScript s) = s := by',
    '  obtain ⟨c, h⟩ := s',
    '  apply Script.ext',
    '  dsimp only [fromScript]',
    '  unfold Script.IsValidCodePoint at h',
    '  simp only [toScript_if, Script_code_if]',
    '  dsimp only [toScript]',
    '  simp only [if_val_eq, if_val_eq2]',
    '  exact h',
    '',
    'end ScriptName',
    '',
    'namespace MaybeUnknownScriptName',
    '',
    'public def toMaybeUnknownScript : MaybeUnknownScriptName → MaybeUnknownScript',
    '  | scriptName name => MaybeUnknownScript.ofScript (ScriptName.toScript name)',
    '  | unknown => default',
    '',
    'public def fromMaybeUnknownScript (s : MaybeUnknownScript) : MaybeUnknownScriptName :=',
    '  if h : s.code = Script.CodePoints.unknown then',
    '    unknown',
    '  else',
    '    scriptName (ScriptName.fromScript ⟨s.code, s.is_valid.resolve_right h⟩)',
    '',
    'theorem fromToMaybeUnknownScriptIso (name : MaybeUnknownScriptName) :',
    '  fromMaybeUnknownScript (toMaybeUnknownScript name) = name := by',
    '  cases name with',
    '  | scriptName n =>',
    '    dsimp [toMaybeUnknownScript, fromMaybeUnknownScript, MaybeUnknownScript.ofScript]',
    '    split',
    '    · rename_i h',
    '      have : (ScriptName.toScript n).code ≠ Script.CodePoints.unknown := by',
    '        cases n <;> decide',
    '      contradiction',
    '    · rw [ScriptName.fromToScriptIso]',
    '  | unknown =>',
    '    dsimp [toMaybeUnknownScript, fromMaybeUnknownScript]',
    '    rfl',
    '',
    'theorem toFromMaybeUnknownScriptIso (s : MaybeUnknownScript) :',
    '  toMaybeUnknownScript (fromMaybeUnknownScript s) = s := by',
    '  obtain ⟨c, hc⟩ := s',
    '  dsimp [fromMaybeUnknownScript, MaybeUnknownScript.ofScript]',
    '  split',
    '  · rename_i h',
    '    subst h',
    '    dsimp [toMaybeUnknownScript]',
    '    rfl',
    '  · rename_i h',
    '    dsimp [toMaybeUnknownScript]',
    '    rw [ScriptName.toFromScriptIso ⟨c, hc.resolve_right h⟩]',
    '    rfl',
    '',
    'end MaybeUnknownScriptName',
    'end Unicode',
    ''
  ].join('\n');
}

