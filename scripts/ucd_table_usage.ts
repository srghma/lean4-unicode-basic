#!/usr/bin/env node

import * as path from 'node:path';
import { repoRoot, relPosix, walkFiles, writeText } from './lib/ucd_report_lib.ts';

const generatedModules = walkFiles(path.join(repoRoot, 'lib', 'UnicodeBasic', 'TableLookupTables'), (p) => p.endsWith('.lean'))
  .map((absolutePath) => relPosix(absolutePath))
  .sort();

let markdown = '';
markdown += `# Data Table Usage\n\n`;
markdown += `Generated from \`UnicodeBasic/TableLookup.lean\`.\n\n`;
markdown += `\`UnicodeBasic.TableLookup\` uses pre-generated Lean modules from \`UnicodeBasic.TableLookupTables\`. These modules are generated by \`table-generators/makeTablesForLookup.lean\` directly from \`UnicodeData\` and UCD parser modules.\n\n`;
markdown += `There is no \`UnicodeCLib\` backend and no extern lookup implementation.\n\n`;
markdown += `## Generated Lookup Modules\n\n`;
for (const file of generatedModules) {
  markdown += `- \`${file}\`\n`;
}

writeText(path.join(repoRoot, 'docs', 'status', 'data-table-usage.md'), markdown);
console.log(`Wrote docs/status/data-table-usage.md for ${generatedModules.length} generated modules.`);
